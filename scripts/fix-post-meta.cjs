// scripts/fix-post-meta.cjs
const fs = require("fs");
const path = require("path");

const metaPath = path.join(__dirname, "..", "data", "post-meta.js");

let raw;
try {
  raw = fs.readFileSync(metaPath, "utf8");
} catch (err) {
  console.error("❌ Cannot read post-meta.js:", err.message);
  process.exit(1);
}

// Match both window. and const definitions
const match = raw.match(/(?:window\.|const\s+)postMetadata\s*=\s*(\{[\s\S]*?\})\s*;/);
if (!match || !match[1]) {
  console.error("❌ Could not extract valid JSON from post-meta.js");
  process.exit(1);
}

let metadata;
try {
  metadata = JSON.parse(match[1]);
} catch (err) {
  console.error("❌ Failed to parse metadata JSON:", err.message);
  process.exit(1);
}

// Rebuild the JS file
const finalOutput = `// Auto-generated by Supreme SEO Engine — Do Not Edit

const postMetadata = ${JSON.stringify(metadata, null, 2)};

if (typeof module !== 'undefined' && module.exports) {
  module.exports.postMetadata = postMetadata;
} else if (typeof window !== 'undefined') {
  window.postMetadata = postMetadata;
}
`;

fs.writeFileSync(metaPath, finalOutput, "utf8");
console.log("✅ Fixed post-meta.js for universal context (Node + Browser)");