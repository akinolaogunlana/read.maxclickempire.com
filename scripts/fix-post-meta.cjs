// update-post-meta.js
const fs = require("fs");
const path = require("path");
const cheerio = require("cheerio");

const postsDir = path.join(__dirname, "..", "posts");
const outputPath = path.join(__dirname, "post-meta.js");

const postMetadata = {};

try {
  const files = fs.readdirSync(postsDir);

  files.forEach((file) => {
    if (!file.endsWith(".html")) return;

    const slug = file.replace(".html", "");
    const filePath = path.join(postsDir, file);
    const html = fs.readFileSync(filePath, "utf8");
    const $ = cheerio.load(html);

    const title = $("head title").text().trim();
    const description = $('meta[name="description"]').attr("content")?.trim() || "";
    const keywords = $('meta[name="keywords"]').attr("content")?.trim() || "";
    const ogImage = $('meta[property="og:image"]').attr("content")?.trim()
      || "https://read.maxclickempire.com/assets/og-image.jpg";

    if (!title || !description) {
      console.warn(`⚠️ Skipped ${file} due to missing title or description.`);
      return;
    }

    const stats = fs.statSync(filePath);
    const published = stats.birthtime.toISOString(); // Actual creation date
    const modified = stats.mtime.toISOString();      // Last modification date

    postMetadata[slug] = {
      title,
      description,
      keywords,
      image: ogImage,
      published,
      modified,
      canonical: `https://read.maxclickempire.com/posts/${file}`
    };
  });

  // Final output to JS file
  const output = `// Auto-generated by Supreme SEO Engine — Do Not Edit

window.postMetadata = ${JSON.stringify(postMetadata, null, 2)};
`;

  fs.writeFileSync(outputPath, output, "utf8");
  console.log("✅ post-meta.js updated successfully.");
} catch (err) {
  console.error("❌ Error updating post metadata:", err);
}